'use client'

import KpiCardComponent from './KpiCard'
import type { KpiCard } from '@/types/charts'

interface KpiGridProps {
  readonly kpis: KpiCard[]
  readonly columns?: 1 | 2 | 3 | 4
  readonly onKpiClick?: (kpi: KpiCard) => void
}

export default function KpiGrid({ kpis, columns = 4, onKpiClick }: Readonly<KpiGridProps>) {
  const gridCols = {
    1: 'grid-cols-1',
    2: 'grid-cols-1 md:grid-cols-2',
    3: 'grid-cols-1 md:grid-cols-2 lg:grid-cols-3',
    4: 'grid-cols-1 md:grid-cols-2 lg:grid-cols-4',
  }

  return (
    <div className={`grid ${gridCols[columns]} gap-6`}>
      {kpis.map((kpi) => (
        <KpiCardComponent
          key={kpi.id}
          kpi={kpi}
          onClick={onKpiClick ? () => onKpiClick(kpi) : undefined}
        />
      ))}
    </div>
  )
}

